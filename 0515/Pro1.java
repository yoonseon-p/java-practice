package basic;

public class Pro1 {
	public static void main(String[] args) {
		// 변수 선언 두개 정수형
		int n1 = 100, n2 = 200;
		// 문자열 + 모든타입 ---> 문자열 + 문자열
		//System.out.println("더하기 : " + n1+n2);
		System.out.println("더하기 : " + (n1+n2));
		System.out.println("빼기 : " + (n1-n2));
		System.out.println("나누기 : " + (n1/n2));
		System.out.println("곱하기 : " + (n1*n2));
	}
}
